#include<stdio.h>
int main()
{
    int a=2;
    float b=3.557;
    char c='a';
    long d=4.666;
    double e=94.64;
    long double f=7.4566854;
    printf("int %d\n",a);
    printf("float %f\n",b);
    printf("char %c\n",c);
    printf("long %lo\n",d);
    printf("double %lf\n",e);
    printf("long double %Lf",f);
    
    return 0;
}
